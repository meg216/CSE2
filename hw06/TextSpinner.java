public class TextSpinner{
    //main method required for all Java programs
    public static void main(String[] args){
        while(true){
            System.out.print("\b"+ "/");   //deletes the previous part and prints each part of the spinner
            System.out.print("\b"+ "-");
            System.out.print("\b"+ "\\");
            System.out.print("\b"+ "|");
        }
 
 
 
 
 
 
        
    }
}